   <div class="timeline">
      <div class="container left">
         <div class="content mdl-color--blue-grey-50">
            <h2>1978</h2>
            <p>Berdiri Badan Koordinasi Kegiatan
               Sosial (BKKS) Propinsi Bengkulu, dengan Akte Notaris Nawawi Bengkulu No
               9 tanggal 8 Agustus 1978 yang dipimpin oleh Ibu Hj Hindun Maurice. BKKS
               bertanggung jawab kepada DNIKS (Dewan Nasional untuk Kesejahteraan
               Sosial) di Jakarta dan Pemerintah Daerah Tk I Bengkulu.
            </p>
         </div>
      </div>
      <div class="container right">
         <div class="content mdl-color--blue-grey-50">
            <h2>1985</h2>
            <p>Gubernur, Kdh Tk I bersama Ka-Kanwil Departemen
               Sosial Propinsi Bengkulu mendirikan lagi Badan Koordinasi Kegiatan
               Kesejahteraan Sosial (BKKKS) Propinsi Bengkulu, yang azas, tujuan serta
               kegiatannya tidak berbeda dengan BKKS. Semenjak berdirinya sampai
               terbentuknya BKKKS Propinsi Bengkulu, BKKS berjalan dengan baik dan
               mendapat support dari organisasi-organisasi yang lain dan status BKKS
               menjadi berubah, sehingga kepengurusannya juga mengalami perubahan.
            </p>
         </div>
      </div>
      <div class="container left">
         <div class="content mdl-color--blue-grey-50">
            <h2>1986</h2>
            <p>Yayasan Dharma Bhakti Kesejahteraan Sosial
               (YDBKS) Propinsi Bengkulu. Pada Kantor Wilayah Departemen Sosial Propinsi
               Bengkulu terdaftar dengan Piagam Ka-Kanwil Departemen Sosial Bengkulu
               tanggal 2 April 1986 No. B IV 4-299/KwSos/1986. Kemudian dikukuhkan oleh
               Menteri Sosial Republik Indonesia dengan SK Menteri Sosial Republik
               Indonesia tanggal 3 Desember 1987 No. 133/KPTS/BBS/ XII/87.
            </p>
         </div>
      </div>
      <div class="container right">
         <div class="content mdl-color--blue-grey-50">
            <h2>1989</h2>
            <p>YDBKS
               menerima sumbangan dari Yayasan ”BIMANTARA” Jakarta sebesar
               60.000.000,- Bertahun-tahun YDBKS berjuang mencari dana untuk mendirikan
               Wisma Tunanetra, akhirnya pada tanggal 5 April 1989 peletakan batu pertama
               dilakukan oleh Bapak dr.D. Amidi, selaku Ketua Kesejahteraan Sosial dari
               Yayasan BIMANTARA Jakarta. Karena sesuatau hal, maka pembangunan
               Wisma Tunanetra itu tidak dapat langsung dilakukan.
            </p>
         </div>
      </div>
      <div class="container left">
         <div class="content mdl-color--blue-grey-50">
            <h2>1990</h2>
            <p>pembangunannya dapat dilakukan dan operasionalnya dimulai pada tanggal 21
               Pebruari 1997, setelah memperoleh bantuan dari BKKKS (Badan Koordinasi
               Kegiatan Kesejahteraan Sosial) Propinsi Bengkulu berupa 12 buah tempat tidur
               kayu lengkap dengan kasur busa,bantal serta sepreinya, (dokumen yayasan
               yang diketik oleh ibu Hj.Hindun Maurice).
            </p>
         </div>
      </div>
      <div class="container right">
         <div class="content mdl-color--blue-grey-50">
            <h2>1999</h2>
            <p>Wisma Tunanetra Amal Mulia berganti nama menjadi
               Panti Sosial Tunanetra Amal Mulia, karena kata Wisma kurang enak didengar
               dan ketika pengurus akan mengajukan bantuan permakanan kepada Dinas
               Sosial Propinsi Bengkulu sekaligus kami ganti, sehingga sampai sekarang kata
               wisma diganti dengan Panti.
            </p>
         </div>
